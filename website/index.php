<font size="+2" color="#000080"> - share your search results</font><br><br>
TeamFound gives a team the capability to share search results without any usage-overhead. The toolbar (firefox and ie) can be used to mark interesting pages and full-text-search those while also showing normal google results for the same keywords.

<h2>Status</h2>
TeamFound is still under development. Our latest alpha-version is available from <a href="https://developer.berlios.de/projects/teamfound">https://developer.berlios.de/projects/teamfound/</a>.

